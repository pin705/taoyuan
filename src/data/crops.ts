/** 根据ID查找作物 */
export const getCropById = (id: string): CropDef | undefined => {
  return CROPS.find(c => c.id === id)
}

/** 根据季节获取可种作物 */
export const getCropsBySeason = (season: string): CropDef[] => {
  return CROPS.filter(c => c.season.includes(season as any))
}
